"""
演示 嵌套应用for循环
"""
# 坚持100天，每天都送10朵花
# range
i = 0  # 纯属为了最后for循环结束后，防止在循环外访问临时变量导致代码不规范，而提前定义
for i in range(1, 101):
    print(f'今天是小美表白的第{i}天，加油，坚持！')
    for j in range(1, 11):
        print(f'给小美送的第{j}朵花')

    print('小美，我喜欢你')  # 内层for循环结束后，在当前外层for循环内的print
print(f'坚持第{i}天，表白成功')