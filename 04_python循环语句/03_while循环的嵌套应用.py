"""
演示while循环的嵌套使用
"""

# 外层：表白100天的控制
# 内层：每天的表白都送10支玫瑰花的控制

i = 1  # 外层循环的控制变量
while i <= 10:  # 外层循环的控制条件
    print(f'今天是第{i}天，准备表白...')

    j = 1  # 内层循环的控制变量
    while j <= 10:  # 内层循环的控制条件
        print(f'送给小妹第{j}支玫瑰花')
        j += 1 # 内层循环的终止条件

    print('小美，我喜欢你')
    i += 1 # 外层循环的终止条件
print(f'坚持到第{i - 1}天，表白成功')