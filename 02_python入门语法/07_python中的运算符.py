# 复合赋值运算符
# +=
num = 1
num += 1  # num = num + 1
print('num + 1: ', num)  # num + 1:  2
num -= 1
print('num -= 1: ', num)  # num -= 1:  1
num *= 4
print('num *= 4: ', num)  # num *= 4:  4
num /= 2
print('num /= 2: ', num)  # num /= 2:  2.0

num = 3
num %= 2
print('num %= 2 ', num)  # num %= 2  1

num **= 2
print('num **= 2: ', num)  # num **= 2:  1

num = 9
num //= 2
print('num //= 2: ', num)  # num //= 2:  4

